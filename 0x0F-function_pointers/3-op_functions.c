#include "3-calc.h"

/**
 * op_add - addition
 * @a: number to add
 * @b: number to be added
 * Return: (a + b)
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substraction
 * @a: number to be substracted
 * @b: number to substract
 * Return: (a - b)
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication
 * @a: number to multiply
 * @b: number to be multiplied
 * Return: (a * b)
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division
 * @a: number to be divided
 * @b: number to divide
 * Return: (a / b)
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - reminder of division
 * @a: number to be divide
 * @b: number to divide
 * Return: (a % b)
 */

int op_mod(int a, int b)
{
	return (a % b);
}
